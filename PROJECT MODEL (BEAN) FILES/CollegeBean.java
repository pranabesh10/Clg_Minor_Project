package org.model.bean;

public class CollegeBean {
	private	String id, name, loc, hcourse, image, rank, web, ai, ugc;

	public String getUgc() {
		return ugc;
	}

	public void setUgc(String ugc) {
		this.ugc = ugc;
	}

	public String getAi() {
		return ai;
	}

	public void setAi(String ai) {
		this.ai = ai;
	}

	public String getWeb() {
		return web;
	}

	public void setWeb(String web) {
		this.web = web;
	}

	public String getRank() {
		return rank;
	}

	public void setRank(String rank) {
		this.rank = rank;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLoc() {
		return loc;
	}

	public void setLoc(String loc) {
		this.loc = loc;
	}

	public String getHcourse() {
		return hcourse;
	}

	public void setHcourse(String hcourse) {
		this.hcourse = hcourse;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
}
